            <h2>Les différentes étapes de la création de la bière</h2>
            <h3>Le maltage</h3>
            <p>
                Le maltage est la première étape lors de la fabrication des bières. Cette étape consiste à préparer les grains (principalment 
                d'orge).
            </p>
            <h3>Le concassage</h3>
            <p>
                La seconde étape est le concassage de nos grains. Lors de cette épate le grain est écrasé et explusé de son enveloppe. 
                Ceci a pour but de le réduire et permettre une meilleure extraction des enzymes et des sucres.
            </p>
            <h3>l'empâtage</h3>
            <p>
                L'empâtage est la première étape du brassage, qui consiste à tremper et remuer (brasser) le malt concassé dans de l'eau 
                chaude afin de procéder à l'extraction de l'amidon contenu dans le malt. Le mélange malt concassé et eau s'appelle la 
                maische. L'opération se nomme « empâtage » en raison de l'aspect pâteux du mélange eau et malt. On ajoute aussi souvent 
                des grains crus ou des flocons (orge, blé, maïs) pour stabiliser le moût et apporter des saveurs supplémentaires.
            </p>
            <h3>La filtration et le rinçage des drêches</h3>
            <p>
                Après l’empâtage, nous enlevons la partie solide appelée drêche de notre partie liquide, le moût. Cette opération a 
                lieu dans la cuve, munie d'un fond filtrant. Ces drêches sont ensuite rincées avec de l'eau chaude afin d'en extraire 
                un maximum de sucres encore présents et ainsi améliorer le rendement. 
            </p>
            <h3>Ebullition et houblonnage</h3>
            <p>
                Une fois les drêches filtrés et rincés, le moût est porté à ébullition pendant plusieurs minutes (entre 1 et 2 heures 
                selon les profils de bières). L’ébullition à 100°C permet de stabiliser et de stériliser le moût mais c'est aussi à 
                cette étape que le houblon est ajouté au moût, il s'agit du houblonnage. Son rôle est important, il fournit au moût, 
                par l'intermédiaire de ses résines, deux acides qui stérilisent, conservent et donnent l'amertume à la bière. On peut 
                l'incorporer en début d’ébullition et/ou à la fin. Les résines amères du houblon sont difficiles à extraire, c'est 
                pourquoi une longue cuisson est nécessaire pour les solubiliser. En fin d'ébullition, cela permet de garder les huiles 
                essentielles du houblon. Cette étape permet également de coaguler les protéines du malt, de favoriser la limpidité et 
                de stériliser le moût.
                On peut aussi ajouter d'autres produits aromatiques (sucres, épices, ingrédients divers), en fonction du type de bière.
            </p>
            <h3>La clarification</h3>
            <p>
                Durant la clarification, nous venons remuer notre moût avec la méthode Whirlpool. Cette méthode consiste à mélanger en 
                formant un tourbillon dans notre cuve, afin de séparer la phase liquide, le moût, de la phase solide, nos résidus de 
                houblon et de protéines. En clarifiant, les résidus solides vont sédimenter dans le fond de la cuve, en son centre. 
                Ainsi, durant la phase de soutirage, nous récupérerons une quantité moindre de résidus solides, qui resteront dans le 
                fond de notre cuve.
            </p>
            <h3>Le refroidissement</h3>
            <p>
                Une fois le moût clarifié, nous allons le refroidir à l’aide d’un serpentin refroidisseur pour pouvoir ensuite inoculer 
                nos levures de notre bière. Le serpentin refroidisseur est un tube en forme de spirale que l’on plonge dans notre cuve 
                dans lequel nous faisons passer de l’eau froide. Les échanges de chaleur entre l’eau et le moût vont permettre à ce 
                dernier de refroidir et d’atteindre une température idéale pour la fermentation. Le moût refroidi est maintenant 
                sensible à toute contamination bactérienne, c'est pourquoi l'hygiène est de rigueur en brasserie.
            </p>
            <h3>Soutirage et ajout des levures</h3>
            <p>
                Maintenant que notre moût est refroidi, nous pouvons ajouter les levures. Il existe deux types de levures, chacunes propres 
                à une fermentation :
            </p>
            <ul>
                <li>
                    La fermentation haute, qui se déroule à une température d'environ 22°C. C'est la méthode de fermentation la plus 
                    ancienne et la plus courante. Les bières de fermentation haute sont appelées "ales".
                </li>
                <li>
                    La fermentation basse, qui se déroule à une température d'environ 12°C. C'est un procédé plus récent, il a été 
                    inventé en 1842, à l'origine pour le type pils (bière blonde). Les bières de fermentation basse sont appelées 
                    "lager".
                </li>
            </ul>
            <h3>Fermentation primaire</h3>
            <p>
                La fermentation primaire est la première fermentation qui se déroule en cuve. Les levures entrent en activité et 
                commencent à transformer les sucres fermentescibles du moût (glucose et maltose) en alcool et en gaz carbonique. 
                Cette réaction libère de l'énergie. La plupart des ferments microorganismes (levures) qui ont la capacité de 
                fermentation alcoolique utilisent cette réaction pour gagner temporairement de l'énergie quand l'oxygène nécessaire 
                à la respiration cellulaire manque.
                <br>
                <br>
                La fermentation primaire va durer environ 7 semaines. Afin de suivre l’avancée de la fermentation, des mesures de 
                densité quotidiennes sont effectuées.
            </p>
            <h3>Sucrage et mise en bouteille</h3>
            <p>
                Après la fermentation, la bière est désormais bonne à être soutirée, c'est-à-dire à être mise en bouteilles. 
                Cependant, si nous souhaitons réaliser une refermentation en bouteille, il est alors nécessaire d’ajouter une petite 
                quantité de sucre dans la bière. On appelle cette étape le sucrage. Le sucre ajouté va être consommé par le reste de 
                levures.
            </p>
            <h3>Fermentation secondaire</h3>
            <p>
                L’objectif de la refermentation en bouteille est la saturation de notre bière en gaz carbonique (CO2), grâce aux levures.
                <br>
                <br>
                Avant la mise en bouteille, les levures ont normalement consommé tous les sucres fermentescibles pour produire de 
                l’alcool et du CO2. La bière, n’étant soumis qu’à la pression atmosphérique, elle n’a pas pu se saturer en CO2, qui 
                s’échappait par le barboteur. L’ajout des sucres fermentescibles va permettre à nouveau de produire du CO2 mais cette 
                fois-ci il ne pourra plus s’échapper. La bière va donc se carbonater progressivement.
            </p>
